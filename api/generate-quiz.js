function buildPrompt(count, questionType) {
  const typeGuide = questionType === 'fill_blank'
    ? 'Chỉ tạo câu hỏi điền vào chỗ trống. Mỗi câu phải có type="fill_blank", answer và acceptedAnswers.'
    : questionType === 'mixed'
      ? 'Tạo kết hợp cả multiple_choice và fill_blank, chia tương đối đều nếu có thể.'
      : 'Chỉ tạo câu hỏi multiple choice. Mỗi câu có 4 lựa chọn và answerIndex.';

  return `
Bạn là AI tutor môn Kinh tế chính trị Mác - Lênin.
Nhiệm vụ: tạo đúng ${count} câu hỏi ôn tập dựa trên COURSE_MATERIAL bên dưới.

COURSE_MATERIAL:
${COURSE_MATERIAL}

Yêu cầu bắt buộc:
- Chỉ dùng nội dung trong COURSE_MATERIAL, không bịa kiến thức ngoài nguồn.
- Viết bằng tiếng Việt, rõ ràng, phù hợp sinh viên ôn tập.
- ${typeGuide}
- Nếu là multiple_choice: dùng cấu trúc {"type":"multiple_choice","topic":"...","question":"...","options":["...","...","...","..."],"answerIndex":0,"explanation":"..."}
- Nếu là fill_blank: dùng cấu trúc {"type":"fill_blank","topic":"...","question":"...","answer":"...","acceptedAnswers":["..."],"explanation":"..."}
- explanation phải giải thích vì sao đúng; nếu có thể, nói ngắn gọn vì sao đáp án sai/chưa hợp lý.
- Không dùng markdown.
- Chỉ trả về JSON hợp lệ theo dạng:
{"questions":[...]}
`;
}

function normalizeQuestion(q, index) {
  const type = q.type === 'fill_blank' ? 'fill_blank' : 'multiple_choice';
  if (type === 'fill_blank') {
    const answer = String(q.answer || q.correctAnswer || '').trim();
    const acceptedAnswers = Array.isArray(q.acceptedAnswers) ? q.acceptedAnswers.map(String).filter(Boolean) : [answer];
    return {
      type,
      topic: String(q.topic || 'AI Tutor'),
      q: String(q.question || q.q || `Câu hỏi ${index + 1}`),
      answer,
      acceptedAnswers,
      explain: String(q.explanation || q.explain || 'AI chưa tạo giải thích cho câu này.')
    };
  }
  return {
    type,
    topic: String(q.topic || 'AI Tutor'),
    q: String(q.question || q.q || `Câu hỏi ${index + 1}`),
    options: Array.isArray(q.options) ? q.options.slice(0, 4).map(String) : [],
    correct: Number.isInteger(q.answerIndex) ? q.answerIndex : Number(q.correct || 0),
    explain: String(q.explanation || q.explain || 'AI chưa tạo giải thích cho câu này.')
  };
}

function isValid(q) {
  if (q.type === 'fill_blank') return Boolean(q.answer || (q.acceptedAnswers && q.acceptedAnswers.length));
  return Array.isArray(q.options) && q.options.length === 4 && q.correct >= 0 && q.correct <= 3;
}

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const body = typeof req.body === 'string' ? JSON.parse(req.body || '{}') : (req.body || {});
    const requestedCount = Number(body.count || 10);
    const count = Math.max(1, Math.min(50, Number.isFinite(requestedCount) ? requestedCount : 10));
    const questionType = ['multiple_choice', 'fill_blank', 'mixed'].includes(body.questionType) ? body.questionType : 'multiple_choice';

    if (!process.env.OPENAI_API_KEY) {
      return res.status(500).json({ error: 'Missing OPENAI_API_KEY environment variable' });
    }

    const openaiRes = await fetch('https://api.openai.com/v1/responses', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${process.env.OPENAI_API_KEY}`
      },
      body: JSON.stringify({
        model: 'gpt-4.1-mini',
        input: buildPrompt(count, questionType),
        text: { format: { type: 'json_object' } }
      })
    });

    const data = await openaiRes.json();
    if (!openaiRes.ok) {
      return res.status(openaiRes.status).json({ error: 'OpenAI API error', detail: data });
    }

    let outputText = data.output_text;
    if (!outputText && Array.isArray(data.output)) {
      outputText = data.output
        .flatMap(item => item.content || [])
        .map(content => content.text || '')
        .join('');
    }

    if (!outputText) {
      return res.status(500).json({ error: 'OpenAI response did not contain text output', detail: data });
    }

    const parsed = JSON.parse(outputText);
    const questions = (parsed.questions || [])
      .slice(0, count)
      .map(normalizeQuestion)
      .filter(isValid);

    if (!questions.length) {
      return res.status(500).json({ error: 'AI did not generate valid questions' });
    }

    return res.status(200).json({ questions, source: 'openai-course-material' });
  } catch (err) {
    return res.status(500).json({ error: 'AI quiz generation failed', detail: err.message });
  }
}
